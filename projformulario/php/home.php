    <h1><strong>Plataforma de inquéritos Cencal.</strong></h1>
    <h5>Esta plataforma é desenvolvida exclusivamente para o uso do <strong>Cencal</strong>, 
    com base dos inquéritos/formulários existentes, o objetivo desta plataforma é 
    a total digitalização dos mesmos anteriormente criados, de modo a dinamizar a utilização e acesso.</h5>
    <h2><strong>Instruções:</strong></h2>
    <h5 style="display: inline;"><strong>1 - Criação e consulta de formulários</strong></h5>
    <br><br>
    <h6><strong>1.1 Criação:</strong></h6>
    <p style="display: inline;">Para aceder á criação de formulários clique em <strong>|? Formulários|</strong>. De seguida clique em 
    <strong>|Criar|</strong>, vai ser redirecionado á página de <strong>Criação do formulários</strong>, 
    de seguida, encontrará um sistema <strong>"Drag and Drop"</strong> com 4 tipos de items: <strong>Botões de escolha</strong> 
    para questões de opção; <strong>Pergunta simples</strong> para respostas simples; <strong>Pontuação</strong>, 
    para questões tipo "avaliação de 1 a 5" e por último <strong>Observações</strong> que permitem respostas discursivas.</p>
    <br><br>
    <h6><strong>1.2 Consulta:</strong></h6>
    <p>Se deseja consultar os formulários criados clique em <strong>|? Formulários|</strong>. De seguida, clique em <strong>|Consultar|</strong>
    e será redirecionado á página de <strong>Consultar do formulários</strong>, onde é disponíbilizado uma seleção de formulários existentes 
    e é possível <strong>Editar</strong>; <strong>Visualizar</strong>; <strong>Enviar</strong>. O envio poderá ser feito para diversos emails simultaneamente; 
    nesse processo é gerado um link 
    no <strong>Gerar link</strong> que vai em anexo no email e permite o acesso aos inqueridos.</p>
    <br>
    <h5><strong>2 - Estatísticas</strong></h5>
    <p>Se deseja consultar as estatísticas clique em <strong>|L Estatísticas|</strong> e será redirecionado á página de <strong>Estatísticas Gráficos</strong>. 
    onde é possível consultar os gráficos criados, selecionando no spinner da página, com isso é possível consultar a média de respostas de cada pergunta.</p>
